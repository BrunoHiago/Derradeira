    public boolean valido(){
        return !(txtCpf.getText().length()<10 ||
                 txtNome.getText().isBlank() ||
                txtSobre.getText().isBlank() ||
                txtTelefone.getText().length() < 10 ||
                txtRenda.getText().isBlank());
    }
