    private void btnNovoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNovoActionPerformed
        DonoCasaDAO conexao = new DonoCasaDAO();
        
        if(!this.valido()){
            txtObs.setText("Possui Campos Invalidados!");
        }else if(conexao.isExist(txtCpf.getText())){
            txtObs.setText("CPF ja existe");
        }else{
            String cpf = txtCpf.getText();
            String nome = txtNome.getText() +" "+txtSobre.getText();
            String telefone = txtTelefone.getText();
            float renda = Float.parseFloat(txtRenda.getText().replace(",", "."));
            
            DonoCasa d = new DonoCasa(cpf, nome,telefone, renda);
            conexao.inserirDonoCasa(d);
            JOptionPane.showMessageDialog(null, "Dono de Casa criada com sucesso!!");
            this.dispose();
            
        }
    }//GEN-LAST:event_btnNovoActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        DonoCasaDAO conexao = new DonoCasaDAO();
        
        if(!this.valido()){
            txtObs.setText("Possui Campos Invalidados!");
        }else if(!conexao.isExist(txtCpf.getText())){
            txtObs.setText("CPF nao existe");
        }else{
            String cpf = txtCpf.getText();
            String nome = txtNome.getText() +" "+txtSobre.getText();
            String telefone = txtTelefone.getText();
            float renda = Float.parseFloat(txtRenda.getText().replace(",", "."));
            
            DonoCasa d = new DonoCasa(cpf, nome,telefone, renda);
            conexao.editarDonoCasa(d);
            JOptionPane.showMessageDialog(null, "Dono de Casa editado com sucesso!!");
            this.dispose();
            
        }
    }//GEN-LAST:event_btnEditarActionPerformed

