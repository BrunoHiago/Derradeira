    private void atualizarTable(){
        ResidenciaDAO conexao = new ResidenciaDAO();
        ArrayList<Residencia> list = conexao.buscarResidencia();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setNumRows(0);
        
        for(Residencia r: list){
            model.addRow(new Object[]{
                    r.getCod(),
                    r.getArea(),
                    r.getEndereco()
            });
        }
    }
