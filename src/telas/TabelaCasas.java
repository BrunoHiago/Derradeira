    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        if(boxCod.isSelected()){
        
            if(txtCod.getText().isBlank()){
                txtObs.setText("Campo Cod vazio");
            }else{
                ResidenciaDAO conexao = new ResidenciaDAO();
                Residencia r = conexao.buscarResidenciaPorCod(Integer.parseInt(txtCod.getText()));
                DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                model.setNumRows(0);

                if(r != null){
                    model.addRow(new Object[]{
                        r.getCod(),
                        r.getArea(),
                        r.getEndereco()
                    });
                }
                txtCod.setText("");
            }
        }else{
            if(txtEndereco.getText().isEmpty()){
                txtObs.setText("Campo Endereço vazio");
            }else{
                ResidenciaDAO conexao = new ResidenciaDAO();
                ArrayList<Residencia> list = conexao.buscarResidencia(txtEndereco.getText());
                DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                model.setNumRows(0);
                
                for(Residencia r:list){
                    model.addRow(new Object[]{
                        r.getCod(),
                        r.getArea(),
                        r.getEndereco()
                    });
                }
                txtEndereco.setText("");
            }
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

